let arr = [2, 3, 4 ];
console.log(arr.length);

arr.push(10); // Add an element at last
console.log(arr); 

arr.pop(10);  // Remove an element at last
console.log(arr); 

arr.unshift(5); // Add an element in start
console.log(arr); 

arr.shift(5); // Remove an element in start
console.log(arr); 

arr.reverse();  // Reverse an array
console.log(arr)

console.log(arr.indexOf(4));